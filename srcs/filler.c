#include "../includes/header.h"

void	filler(t_filler *f)
{
	int		i;
	int		j;


	j = -1;
	while (++j < f->size_y)
	{
		i = MARGIN - 1;
		while (++i < f->size_x)
		{
			if (f->tab[j][i] == 'o')
				ft_putstr("1 1\n");
			else if (f->tab[j][i] == 'x')
				ft_putstr("2 2\n");
		}
	}
	ft_putstr("8 1\n");
}
